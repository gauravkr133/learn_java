
public class Loops{
	public static void main(String[] args){
		//Loops
		for(int i=0;i<=100;i++){
			System.out.println(i);
		}
		
		//While Loop
		int i = 100;
		while(i>=1){
			System.out.println(i);
			i--;
		}
		//do while loop
		int k = 100;
		do{
			System.out.println(k);
			k--;
		}while(k>=10);
	}
}